def diagonalDifference(arr):
    length = len(arr)
    result = 0
    for i in range(length):
        result += arr[i][i]
    
    j = length-1
        
    for i in range(length):
        result -= arr[j][i]
        j-=1
    
    result = abs(result)
    return result 
    
    
        
